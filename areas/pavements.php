<h4>Pavements</h4>
UTEP has an extensive portfolio of projects related to design, construction quality management, material testing, pavement characterization and recycling. <br/><br/>
  
  <strong><em>Design</em></strong><br/><br/>
  
  A number of advanced initiatives related to design of pavements have been carried out at CTIS.  Examples of recent projects include:<br/> 
  <ul>
    
    <li>	Realistic Design Guidelines for Low Classification Roads in High PI Clays, TxDOT and FHWA</li>
    <li>Next-Generation Finite Element Pavement Analysis Software NYSDOT and FHWA</li>
    <li>Verification of the Modified Triaxial Design Procedure, TxDOT and FHWA</li>
    <li>Design Modulus Values Using Seismic Data Collection, TxDOT and FHWA</li>
    <li>Development of a Comprehensive, Rational Method for Determination of Remaining Life of Existing Pavements, TxDOT and FHWA </li>
  </ul>
  
  To carry out these projects in a successful manner, the researchers have expertise in utilizing existing software and developing new software packages.  Examples of software packages used are the new mechanistic-empirical design guide, ABAQUS, LS-DYNA, and ISLAB2000.  In addition, under a FHWA pooled-fund study, a user-friendly finite element package has been developed to estimate the potential damage to pavements due to overweight trucks.<br/><br/>
  
  <strong><em>Construction Quality Management</em></strong><br/><br/>
  
  Examples of recent projects related to this topic include:<br/>
  <ul>
    
    <li>Modulus-Based Construction Specification for Compaction of Earthwork and Unbound Aggregate, National Cooperative Highway Research Program</li>
    <li>Detection of Debonding of Hot Mix Asphalt layers, Airport Asphalt Technology Program</li>
    <li>Nondestructive Testing Technology for Quality Control and Acceptance of Flexible Pavement Construction, FHWA</li>
    <li>Use of NDT Equipment for Construction Quality Control of Hot Mix Asphalt, Arizona DOT</li>
    <li>Innovative Testing Standards for Acceptance Criteria for Concrete Pavement, Innovative Pavement Research Foundation</li>
    <li>Impacts of Construction Quality on Life Cycle Performance of Pavements Using Mechanistic Analysis, TxDOT and FHWA</li>
    <li>Development of Structural Field Testing of Flexible Pavement Layers, TxDOT and FHWA</li>
    <li>Development of Methods and Materials to Accelerate Construction of PCC Pavements, TxDOT and FHWA</li>
  </ul>
  The highlight of these projects is a new software package that can be used to plan and execute the frequency of inspection based on the importance of a layer to the performance of the pavement and the quality of the process control of the contractor.  Also a layer by layer quality management of the pavement during construction based on non-nuclear technology has been developed that is currently being implemented.  A device has been developed to evaluate the quality of Tack coat in the field and has been included in TxDOT specifications.<br/><br/>
  
