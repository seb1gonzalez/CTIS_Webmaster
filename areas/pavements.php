  <em><strong>Pavement Managemen</strong></em>t<br/><br/>
  
  UTEP has developed comprehensive models to implement pavement management systems as decision support tools for agencies to better manage their pavement assets. These models involve the development of practical methods to assess current pavement condition, performance models to forecast future condition, techniques to conduct budget needs and scenario analyses, and reporting tools to communicate budget needs for strategic planning. <br/><br/>
  
  Agencies have largely improved their pavement management practices due to the implementation of systems based on models developed at UTEP. Areas of expertise include:<br/>
  <ul>
    
    <li>Developing a Systematic Approach for Managing Pavement Network Inventory</li>
    <li>Assessing Pavement Condition at Network and Project Level</li>
    <li>Identifying Candidate Pavement Sections for Maintenance and Rehabilitation Treatments</li>
    <li>Determining Budget Needs to Meet Network-Level Performance Goals Set by Agencies</li>
    <li>Prioritizing Projects Needing Maintenance and Rehabilitation when Funds are Constrained </li>
    <li>Conducting Scenarios Analyses to Assess the Impact of Maintenance and Rehabilitation Strategies</li>
    <li>Developing Reports to Communicate Maintenance and Rehabilitation Budget Needs to Funding Authorities.</li>
  </ul>
  
  The most recent project in pavement management is with the Metropolitan Transportation Commission (MTC) of the San Francisco Bay Area in California. A multi-objective pavement management approach to support the implementation of MTCs strategic plan is being developed by UTEP.  The purpose of this project is to assist MTC in the implementation of a strategic management plan that focuses on identifying the minimum budget needed to meet network-level performance goals over the planning horizon.<br/><br/>
  
